public class MediaQuatroNumeros {
    public static void main(String[] args) {
        // Definindo os quatro números
        int num1 = 35;
        int num2 = 42;
        int num3 = 87;
        int num4 = 11;

        // Calculando a média
        double media = (num1 + num2 + num3 + num4) / 4.0;

        // Exibindo o resultado na tela
        System.out.println("A média dos quatro números é: " + media);
    }
}
